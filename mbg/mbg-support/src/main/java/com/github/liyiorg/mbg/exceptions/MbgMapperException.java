package com.github.liyiorg.mbg.exceptions;

public class MbgMapperException extends MbgException {

	private static final long serialVersionUID = 6345421071481243178L;

	public MbgMapperException() {
		super();
	}

	public MbgMapperException(String message, Throwable cause) {
		super(message, cause);
	}

	public MbgMapperException(String message) {
		super(message);
	}

	public MbgMapperException(Throwable cause) {
		super(cause);
	}

}
