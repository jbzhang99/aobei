package com.github.liyiorg.mbg.exceptions;

public class MbgExampleException extends MbgException {

	private static final long serialVersionUID = 6117517996762691536L;

	public MbgExampleException() {
		super();
	}

	public MbgExampleException(String message, Throwable cause) {
		super(message, cause);
	}

	public MbgExampleException(String message) {
		super(message);
	}

	public MbgExampleException(Throwable cause) {
		super(cause);
	}

}
